        {
            string name = "Blade Blaster Strike";
            string description = "Jason pulls out his Blade Blaster from his holder, and fires a shot at this enemy";
            int damage = 35;
        }
        public void Move_2()
        {
            string name = "Power Sword Slash";
            string description = "Jason powers up his Power Sword with the energy of his Tyrannosaurus Dinozord and slashes the enemy!";
            int damage = 50;
        }
        public void Move_3()
